function gradient(text) {

  const colors = [

    chalk.rgb(255, 0, 150),

    chalk.rgb(255, 80, 0),

    chalk.rgb(255, 200, 0),

    chalk.rgb(0, 200, 255),

    chalk.rgb(120, 0, 255)

  ];

  return text

    .split("")

    .map((c, i) => colors[i % colors.length](c))

    .join("");

}

